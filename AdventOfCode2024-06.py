def fillMap(map):
    r=len(map)
    c=len(map[0])
    i=0
    j=0
    (i,j)=findPos(map,r,c)
    direction=map[i][j]
    
    while (i>=0 and i<r and j>=0 and j<c):
        x=map[i][j]
        map[i][j]=ord('X')
        if direction==ord('v'):
            if i+1==r:
                return(map)
            if map[i+1][j]==ord('#'):
                direction=ord('<')
            else:
                i+=1
        elif direction==ord('<'):
            if j-1==-1:
                return(map)
            if map[i][j-1]==ord('#'):
                direction=ord('^')
            else:
                j-=1
        elif direction==ord('^'):
            if i-1==-1:
                return(map)
            if map[i-1][j]==ord('#'):
                direction=ord('>')
            else:
                i-=1
        else:
            if j+1==c:
                return(map)
            if map[i][j+1]==ord('#'):
                direction=ord('v')
            else:
                j+=1

def findPos(map,r,c):
    for i in range(r-1):
        for j in range(c-1):
            x=map[i][j]
            if x==ord('v') or x==ord('<') or x==ord('>') or x==ord('^'):
                return (i,j)

def convert(map):
    newMap=[]
    newMap.append([])
    n=0
    for x in map:
        if x=='\n':
            newMap.append([])
            n+=1
        else:
            newMap[n].append(ord(x))
    return newMap

